/**
 * Resolve the display label for a config field path. Uses locale-specific
 * CONFIG_FIELD_LABELS; falls back to the provided fallback (e.g. hint.label from backend).
 */
export function getConfigFieldLabel(path: Array<string | number>, fallback: string): string {
  const locale = getLocale();
  const labels = CONFIG_FIELD_LABELS[locale];
  for (const key of lookupKeys(path)) {
    const label = labels[key];
    if (label) {
      return label;
    }
  }
  return fallback;
}
